public class Utility {
    public int addThree(int a, int b, int c) {
        return a + b + c;
    }

    // MODIFICACIÓN: Método actualizado para tres parámetros
    /**
     * Resta el segundo y tercer parámetro del primero (a - b - c).
     */
    public int subtractThree(int a, int b, int c) {
        return a - b - c;
    }
}